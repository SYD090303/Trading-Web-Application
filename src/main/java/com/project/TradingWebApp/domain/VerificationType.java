package com.project.TradingWebApp.domain;

/**
 * Enum representing the different types of verification methods
 * that can be used for two-factor authentication (2FA).
 */
public enum VerificationType {

    /** Verification via mobile (e.g., SMS or authentication app). */
    MOBILE,

    /** Verification via email (e.g., OTP sent to the registered email). */
    EMAIL
}




